yourScore = input("Please enter your grade: ")

if yourScore >= "90":
    print("your score is an A", int(yourScore))
elif yourScore >= "80":
    print("Your score is a B", int(yourScore))
elif yourScore >= "70":
    print("Your score is a C", int(yourScore))
elif yourScore >= "60":
    print("Your score is a D", int(yourScore))
elif yourScore <= "559":
    print("Your score is a F", int(yourScore))
